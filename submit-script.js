// Function that creates and opens a popup form at given lat/lng
function showFormPopup(latlng, buildingName) {
  const formHTML = `
    <form id="pointForm" class="popup-form">
      <label><b>${buildingName}</b></label>

      <label>Number of microwaves in this room</label>
      <input type="number" name="quantity" min="0" step="1">

      <label>Floor number</label>
      <input type="number" name="floor">

      <label>Room name + number</label>
      <input type="text" name="room">

      <label>Is this space restricted by key card during regular working hours?</label>
      <select name="key">
        <option value="restricted">restricted</option>
        <option value="unrestricted">unrestricted</option>
      </select>

      <label>More details (current working condition, access restritcted to whom?, general comments, etc...):</label>
      <textarea name="note" style="max-height:100px; min-width:300px; max-width:300px;"></textarea>

      <button type="submit">SUBMIT!</button>
    </form>`;

  // Create the popup and attach it to the map
  const popup = L.popup({ autoPan: false })
    .setLatLng(latlng)
    .setContent(formHTML)
    .openOn(map);

  // Wait for the popup to load into the DOM before adding submit listener
  map.once('popupopen', () => {
    const form = document.getElementById('pointForm');
    if (!form) return;

    // Handle the form submission
    form.addEventListener('submit', ev => {
      ev.preventDefault();
      const fd = new FormData(form);
      const entry = Object.fromEntries(fd.entries());
      const lat = latlng.lat.toFixed(6);
      const lng = latlng.lng.toFixed(6);

      // Send data to Google Form (using buildingName from GeoJSON)
      sendToForm(buildingName, entry.quantity, lat, lng, entry.floor, entry.room, entry.key, entry.note);

      // Add a marker at the clicked location showing what was submitted
      L.marker([lat, lng])
        .bindPopup(`<b>${buildingName}</b><br>Floor ${entry.floor || '-'}, Room ${entry.room || '-'}`)
        .addTo(map);

      map.closePopup();
      alert("Submission sent!");
    });
  });
}

